package ja111.day2;

public class LogicalOperators {
    public static void main(String[] args){
        int a=10;
        int b=2;
        System.out.println(a&b);
        System.out.println(a|b);
        System.out.println(a^b);
        System.out.println(a<<b);
        System.out.println(a>>b);
    }
}


