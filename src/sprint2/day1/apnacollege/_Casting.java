package sprint2.day1.apnacollege;
import java.util.Arrays;


public class _Casting {
    public static void main(String[] args){
        //implicit casting
        double price = 100.00;
        double finalPrice = price +18;
        System.out.println(finalPrice);


        //Explicit Casting
        int p = 100;
        int fp = p + (int)18.18;
        System.out.println(fp);
    }
}
